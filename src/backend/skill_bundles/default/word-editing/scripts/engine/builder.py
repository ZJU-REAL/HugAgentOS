from ._handle import input_path, output_path
from .styles import (
    apply_cjk_font_to_para,
    fonts_for_language,
    set_document_default_fonts,
    setup_heading_styles,
)


def _new_document(language: str = "zh"):
    """Create a python-docx Document with CJK fonts pre-configured."""
    from docx import Document

    doc = Document()
    body_font, heading_font = fonts_for_language(language)
    set_document_default_fonts(doc, body_font)
    setup_heading_styles(doc, heading_font)
    return doc


def create_document(
    *,
    filename: str,
    title: str = "",
    language: str = "zh",
) -> dict[str, Any]:
    """Create a new .docx with optional title heading and 方正 CJK fonts.

    Args:
        filename: output filename (relative to sandbox cwd; sidecar auto-collects)
        title: if non-empty, added as a top-level Title-styled paragraph
        language: ``zh`` (default, 方正 fonts) or ``en`` (Calibri fallback)

    Returns:
        ``{"output_filename": str, "title": str, "language": str,
           "size_bytes": int, "paragraph_count": int}``
    """
    body_font, _heading_font = fonts_for_language(language)
    doc = _new_document(language)

    if title:
        para = doc.add_heading(title, level=0)
        # heading style already configured by setup_heading_styles; ensure CJK applied to title runs
        apply_cjk_font_to_para(para, _heading_font)

    out = output_path(filename)
    out.parent.mkdir(parents=True, exist_ok=True)
    doc.save(str(out))

    size = out.stat().st_size
    return {
        "output_filename": filename,
        "title": title,
        "language": language,
        "size_bytes": size,
        "paragraph_count": len(doc.paragraphs),
    }


def add_heading(
    *,
    input_filename: str,
    output_filename: str,
    text: str,
    level: int = 1,
) -> dict[str, Any]:
    """Open ``input_filename``, append a heading, save as ``output_filename``."""
    from docx import Document

    if not (1 <= level <= 6):
        raise ValueError(f"level must be 1-6, got {level}")

    doc = Document(str(input_path(input_filename)))
    para = doc.add_heading(text, level=level)
    # Re-apply CJK so newly-added heading respects existing language choice.
    # We don't know which language the source was authored in; default to 方正
    # which is the dominant case for this project. English-only documents can
    # have set_document_default_fonts re-applied via a separate tool if needed.
    from .styles import HEADING_FONT
    apply_cjk_font_to_para(para, HEADING_FONT)

    out = output_path(output_filename)
    out.parent.mkdir(parents=True, exist_ok=True)
    doc.save(str(out))

    return {
        "output_filename": output_filename,
        "added_heading": text,
        "level": level,
        "paragraph_count": len(doc.paragraphs),
    }


def _parse_markdown_table(markdown: str) -> list[list[str]]:
    """Parse a single markdown table block into rows of cell strings.

    Strips a separator row (``|---|---|``) if present. Empty input returns ``[]``.
    """
    import re as _re

    rows: list[list[str]] = []
    for line in (markdown or "").splitlines():
        s = line.strip()
        if not (s.startswith("|") and s.endswith("|") and s.count("|") >= 2):
            continue
        cells = [c.strip() for c in s.strip("|").split("|")]
        non_empty = [c for c in cells if c]
        if non_empty and all(_re.match(r"^:?-+:?$", c) for c in non_empty):
            continue  # separator row
        rows.append(cells)
    return rows


