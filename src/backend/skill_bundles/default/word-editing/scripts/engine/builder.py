"""Word document creation primitives.

These functions are intended to be called from a sandbox-side Python script
generated by ``mcp_servers/_office_shared/script_builder.py``. They write the
output ``.docx`` to ``engine.output_path(filename)`` so the
sandbox sidecar auto-collects the file.

Each function returns a small dict with metadata that the MCP tool surfaces
back to the LLM (e.g. file size, paragraph count). The actual file bytes
travel through the sandbox file-collection channel, not through ``data``.
"""
from __future__ import annotations

import io
